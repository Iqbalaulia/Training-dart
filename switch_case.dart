void main(){
  var nilai = 'F';
  switch (nilai) {
    case 'A':
      print('Sangat baik');
      break;
    case 'B':
      print('Lumayan baik');
      break;
    case 'C':
      print('Baik');
      break;
    case 'D':
      print('Kurang baik');
      break;
    
    default:
      print('Tidak valid');
  }
}