void main(){
  // tipe data number

  // num dapat menyimpan int atau double
  num angka = 20;

  // tidak dapat menyimpan double
  int angka1 = 20;
  
  double angka2 = 20.2;

  print('angka-${angka.runtimeType}');
  print('angka1-${angka1.runtimeType}');
  print('angka2-${angka2.runtimeType}');

}