// operator matematika

void main(){
  int panjang = 10;
  int lebar = 20;
  int tinggi = 10;
  
  // formula kelliling persegi soal 1
  int rumusKelilingPersegi = 2 * (panjang + lebar);
  print('keliling persegi = ${rumusKelilingPersegi}');

  // formula luas lingkaran
  double pi = 22/7;
  int r = 10;
  double rumusLuasLingkaran = pi * (r * r);
  print('luas lingkaran = ${rumusLuasLingkaran}');

  // formula menghitung volume balok
  int rumusVolumeBalok = panjang * lebar * tinggi;
  print('volume balok = ${rumusVolumeBalok}');

}